CSEG AT 0

ACALL DELAY1S	; 2US
SJMP LAST

DELAY1MS:	;1000US

MOV R7,#255	;1US
DJNZ R7,$		;2US*255=510US
MOV R7,#243	;1US
DJNZ R7,$	;2US*243=486US
RET		;TOTAL=998US

DELAY10MS:	;10000US

MOV R6,#9	;1US
DL1:
ACALL DELAY1MS	;2US*9=18US+ 9*998=8982==9000
DJNZ R6,DL1	;2US*9=18US
MOV R6,#86	;1US
DJNZ R6,$	;2US*86=172US

RET

DELAY100MS:	;100000US

MOV R5,#9
DL2:
ACALL DELAY10MS
DJNZ R5,DL2
MOV R5,#9
DL3:
ACALL DELAY1MS
DJNZ R5,DL3
MOV R5,#85
DJNZ R5,$
NOP
RET

